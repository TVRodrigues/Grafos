package algoritmosgrafos.util;

public class Aresta {
    
    private int u; //Vértice origem
    private int v; //Vértice destino
    private int w; //peso

    public Aresta(int u, int v, int w) {
        this.u = u;
        this.v = v;
        this.w = w;
    }

    public int getU() {
        return u;
    }

    public void setU(int u) {
        this.u = u;
    }

    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }
    
    
    
    
    
    
}







